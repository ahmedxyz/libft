#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*fs;
	size_t	i;

	if (!s || !f)
		return (NULL);
	fs = ft_strdup(s);
	if (!fs)
		return (NULL);
	i = 0;
	while (s[i])
	{
		fs[i] = f(i, s[i]);
		i++;
	}
	fs[i] = '\0';
	return (fs);
}
