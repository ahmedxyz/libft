#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char	*tmp;
	size_t	i;
	size_t	j;

	tmp = s1;
	i = ft_strlen(s1);
	j = 0;
	while (s2[j])
	{
		s1[i + j] = s2[j];
		j++;
	}
	s1[i + j] = '\0';
	return (tmp);
}
